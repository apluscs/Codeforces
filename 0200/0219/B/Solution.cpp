const int mod = 1000000009;

ll p, d;
struct Solution {
  ll solve() {
    ll base = 1, res = p;
    bool good = true;
    while (good) {
      ll x = p / base % 10;
      while (x != 9) {
        p -= base, d -= base;
        if (d < 0) {
          good = false;
          break;
        }
        x = p / base % 10;
      }
      if (good) res = p;
      base *= 10;
    }
    return res;
  }
};
